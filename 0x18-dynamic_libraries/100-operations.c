/**
 * add - addition
 * @a: first parameter
 * @b: second parameter
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction
 * @a: first parameter
 * @b: second parameter
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication
 * @a: first parameter
 * @b: second parameter
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @a: first parameter
 * @b: second parameter
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - divisible
 * @a: first parameter
 * @b: second parameter
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
